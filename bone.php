   <h3>Bone Marrow Transplant</h3>
<p>Blood and Marrow Transplantation is a form of intensive treatment used to treat certain cancer like leukaemias, Lymphomas and some Non-cancerous Diseases like thalassaemia.
 Bone marrow is found inside our bones, and is the factory that make blood. 
 It is responsible for producing white blood cells (to protect against infection), Red blood cells (to carry oxygen around the body) and platelets (to prevent bleeding).
 Stem cells are blood cells at their earliest stage of development in the bone marrow, before they have become committed to developing into white cells, red cells or platelets.
 It is these stem cells which are the key Factors in transplantation. 
 They replace the diseased marrow as well as provide immunity against cancer and infections.</p>
 <h4>There are two main types of Transplants</h4>
 <h5>Autologous Transplant:</h5>
 <p>This means that the bone marrow or stem cells used for the transplant are ones own.
  A little bit of the Patients bone marrow or peripheral blood stem cells is taken and stored before high dose treatment.
  When the treatment is over, the bone marrow or stem cells are given back through a vein.</p>
<h5>Allogeneic Transplant:</h5>
<p>In this type of transplant, bone marrow donated by someone else is used. It is essential that the donors tissue matches. The most suitable donor is usually a close relative, most commonly a brother or sister.
 Rarely, Parents can also be the source for haploidentical transplants.
 It is also possible to get a good match from an unrelated donor from international registries.
 Umblical cord blood stem cells from public cord banks is also available in India.</p>
    
